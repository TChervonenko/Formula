function areaOfSquare (lengthOfSideOfSquare) {
    return lengthOfSideOfSquare*lengthOfSideOfSquare;
}

function areaOfRectangle (lengthOneSideRectangle, lengthSecondSideRectangle ) {
    return lengthOneSideRectangle*lengthSecondSideRectangle;
}

function areaOfParalelograma (lengthOfSideParalelograma, lengthOfHeightParalelograma ) {
    return lengthOfSideParalelograma*lengthOfHeightParalelograma;
}

function areaOfRomba (lengthOfSideRomba, lengthOfHeightRomba ) {
    return lengthOfSideRomba*lengthOfHeightRomba;
}
